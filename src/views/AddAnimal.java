    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        String name = animalNameTxt.getText();
        String age = animalAgeTxt.getText();
        String cost = animalCostTxt.getText();
        String gender = animalGenderTxt.getText();
        String species = animalSpeciesTxt.getText();
        String additional = additionalTxt.getText();
        String habitat = animalHabitatTxt.getText();
        
        if(!rdnBird.isSelected() && !rdnFish.isSelected())
        {
            errorLbl.setText("Error: Please select a type of animal");
        }
        else if(name.equals("") || age.equals("") || cost.equals("") || gender.equals("") || species.equals("") || additional.equals("") || habitat.equals(""))
        {
            errorLbl.setText("Error: Please complete ALL fields");
        }
        else{
            try
            {
                int intAge = Integer.parseInt(age);
                try
                {
                    double doubleCost = Double.parseDouble(cost);
                    if(gender.length() != 1)
                    {
                        errorLbl.setText("Error: Please enter M or F for gender");
                    }
                    else
                    {
                        char charGender = gender.charAt(0);
                        if(rdnBird.isSelected())
                        {
                            if(additional.equalsIgnoreCase("True"))
                            {
                                boolean canFly = true;
                                
                                //(String nameIn, String typeIn, int ageIn, String habitatIn, double costIn, boolean isAvailableIn, char genderIn, boolean canFlyIn){
                                Bird bird = new Bird(name, species, intAge, habitat, doubleCost, true, charGender, canFly);
                                DBManager db = new DBManager();
                                db.addAnimal(bird);
                                
                                errorLbl.setText("New Bird added");
                            }
                        
                        else if(additional.equalsIgnoreCase("False"))
                        {
                            boolean canFly = false;
                            Bird bird = new Bird(name, species, intAge, habitat, doubleCost, true, charGender, canFly);
                            DBManager db = new DBManager();
                            db.addAnimal(bird);
                            
                            errorLbl.setText("New Bird added");
                        }
                        else
                        {
                            errorLbl.setText("Error: Enter true/false ONLY");
                        }
                    }
                    
                    else
                        {
                         //(String nameIn, String typeIn, int ageIn, String habitatIn, double costIn, boolean isAvailableIn, char genderIn, String waterTypeIn){
                         Fish fish = new Fish(name, species, intAge, habitat, doubleCost, true, charGender, additional);
                         DBManager db = new DBManager();
                         db.addAnimal(fish);
                         
                         errorLbl.setText("New Fish added");
                    }
                }
                }
                catch(Exception ex)
                {
                    errorLbl.setText("Error: Please enter numbers only for cost");
                }
            }
            catch(Exception ex)
            {
                errorLbl.setText("Error: Please enter a number only for age");
            }
        }
    }//GEN-LAST:event_addBtnActionPerformed

